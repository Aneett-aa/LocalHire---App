import 'package:flutter/material.dart';
import 'package:firebase_auth/firebase_auth.dart';
import 'package:cloud_firestore/cloud_firestore.dart';
import 'package:firebase_auth/firebase_auth.dart';
import 'package:cloud_firestore/cloud_firestore.dart';

class ProfileScreen extends StatefulWidget {
  const ProfileScreen({super.key});

  @override
  State<ProfileScreen> createState() => _ProfileScreenState();
}

class _ProfileScreenState extends State<ProfileScreen> {

  bool isHiring = false;

  final Color primaryGold = const Color(0xFFFFB544);
  final Color lightCream = const Color(0xFFFFE7BF);
  final Color localRed = const Color(0xFFE53935);

List<Map<String, dynamic>> reviews = [];
double averageRating = 0.0;
bool reviewLoading = true;

  Map<String, dynamic>? userData;
  bool isLoading = true;

  @override
  void initState() {
    super.initState();
    fetchUserData();
    fetchReviews();
  }

  Future<void> fetchUserData() async {
    final user = FirebaseAuth.instance.currentUser;

    if (user != null) {
      final doc = await FirebaseFirestore.instance
          .collection("users")
          .doc(user.uid)
          .get();

      if (doc.exists) {
        setState(() {
          userData = doc.data();
          isLoading = false;
        });
      }
    }
  }

  Future<void> fetchReviews() async {
  final user = FirebaseAuth.instance.currentUser;

  if (user == null) return;

  final query = await FirebaseFirestore.instance
      .collection("reviews")
      .where("toUserId", isEqualTo: user.uid)
      .get();

  double total = 0;
  reviews.clear();

  for (var doc in query.docs) {
    final data = doc.data();
    reviews.add(data);
    total += (data["rating"] ?? 0);
  }

  if (reviews.isNotEmpty) {
    averageRating = total / reviews.length;
  }

  setState(() {
    reviewLoading = false;
  });
}

  @override
  Widget build(BuildContext context) {

    if (isLoading) {
      return const Scaffold(
        body: Center(child: CircularProgressIndicator()),
      );
    }

    final name = userData?["name"] ?? "";
    final location = userData?["location"] ?? "";
    final profileImage = userData?["profileImage"];
    final skills = List<String>.from(userData?["skills"] ?? []);
    final createdAt = userData?["createdAt"];

    String memberSince = "";
    if (createdAt != null) {
      final date = createdAt.toDate();
      memberSince =
          "${date.month}/${date.year}";
    }

    return Scaffold(
      backgroundColor: const Color(0xFFF7F7F7),

      body: SafeArea(
        child: SingleChildScrollView(
          padding: const EdgeInsets.symmetric(horizontal: 20),
          child: Column(
            children: [

              const SizedBox(height: 10),

              /// HEADER
              Row(
                mainAxisAlignment: MainAxisAlignment.spaceBetween,
                children: [
                  IconButton(
                    icon: const Icon(Icons.arrow_back),
                    onPressed: () => Navigator.pop(context),
                  ),
                  const Text(
                    "Profile",
                    style: TextStyle(
                        fontSize: 20,
                        fontWeight: FontWeight.bold),
                  ),
                  const Icon(Icons.settings),
                ],
              ),

              const SizedBox(height: 20),

              /// PROFILE IMAGE
              Stack(
                alignment: Alignment.bottomRight,
                children: [
                  Container(
                    padding: const EdgeInsets.all(4),
                    decoration: BoxDecoration(
                      border: Border.all(color: primaryGold, width: 4),
                      shape: BoxShape.circle,
                    ),
                    child: CircleAvatar(
                      radius: 55,
                      backgroundImage: profileImage != null
                          ? NetworkImage(profileImage)
                          : null,
                    ),
                  ),
                  
                ],
              ),

              const SizedBox(height: 15),

              Text(
                name,
                style: const TextStyle(
                    fontSize: 22,
                    fontWeight: FontWeight.bold),
              ),

              const SizedBox(height: 5),

              Text(
                "$location • Member since $memberSince",
                style: const TextStyle(color: Colors.grey, fontSize: 13),
              ),

              const SizedBox(height: 25),

              /// ACTION BUTTONS
              Row(
                children: [
                  Expanded(
                    child: ElevatedButton.icon(
                      style: ElevatedButton.styleFrom(
                        backgroundColor: primaryGold,
                        padding: const EdgeInsets.symmetric(vertical: 14),
                        shape: RoundedRectangleBorder(
                            borderRadius: BorderRadius.circular(12)),
                      ),
                      onPressed: () {},
                      icon: const Icon(Icons.edit),
                      label: const Text("Edit Profile"),
                    ),
                  ),
                  const SizedBox(width: 10),
                  Expanded(
                    child: OutlinedButton.icon(
                      style: OutlinedButton.styleFrom(
                        side: BorderSide(color: primaryGold),
                        padding: const EdgeInsets.symmetric(vertical: 14),
                        shape: RoundedRectangleBorder(
                            borderRadius: BorderRadius.circular(12)),
                      ),
                      onPressed: () {},
                      icon: Icon(Icons.share, color: primaryGold),
                      label: Text(
                        "Share",
                        style: TextStyle(color: primaryGold),
                      ),
                    ),
                  ),
                ],
              ),

              const SizedBox(height: 25),

              /// TOGGLE
              Container(
                padding: const EdgeInsets.all(4),
                decoration: BoxDecoration(
                  color: Colors.grey.shade200,
                  borderRadius: BorderRadius.circular(12),
                ),
                child: Row(
                  children: [
                    _toggleButton("Hiring", true, Icons.search),
                    _toggleButton("Working", false, Icons.work),
                  ],
                ),
              ),

              const SizedBox(height: 30),

              /// ABOUT
              _sectionTitle("About"),
              const SizedBox(height: 8),
              Text(
                isHiring
                    ? "Regularly looking for reliable help with home maintenance and specialized tasks."
                    : "Experienced LocalHire worker with skills in ${skills.join(", ")}.",
                style: const TextStyle(height: 1.5),
              ),

              const SizedBox(height: 30),

              _sectionTitle(isHiring ? "Employer Stats" : "Worker Stats"),
              const SizedBox(height: 12),
              _statCard("—", isHiring ? "Jobs Posted" : "Jobs Completed"),

              const SizedBox(height: 30),

              _sectionTitle(isHiring ? "Employer Feedback" : "Reviews"),
              const SizedBox(height: 15),
              _reviewCard(),

              if (!isHiring) ...[
                const SizedBox(height: 30),
                _sectionTitle("Services Offered"),
                const SizedBox(height: 10),
                Wrap(
                  spacing: 8,
                  runSpacing: 8,
                  children: skills
                      .map((skill) => _serviceChip(skill))
                      .toList(),
                ),
              ],

              const SizedBox(height: 40),

              _bigButton("SOS EMERGENCY", localRed),
              const SizedBox(height: 12),
              _bigButton("LOGOUT", Colors.black),

              const SizedBox(height: 60),
            ],
          ),
        ),
      ),
    );
  }

  /// ---------------- HELPERS ----------------

  Widget _toggleButton(String text, bool value, IconData icon) {
    final selected = isHiring == value;

    return Expanded(
      child: GestureDetector(
        onTap: () => setState(() => isHiring = value),
        child: Container(
          padding: const EdgeInsets.symmetric(vertical: 10),
          decoration: BoxDecoration(
            color: selected ? Colors.white : Colors.transparent,
            borderRadius: BorderRadius.circular(10),
            boxShadow: selected
                ? const [
                    BoxShadow(
                        blurRadius: 4,
                        color: Colors.black12,
                        offset: Offset(0, 2))
                  ]
                : [],
          ),
          child: Row(
            mainAxisAlignment: MainAxisAlignment.center,
            children: [
              Icon(icon,
                  size: 16,
                  color: selected ? Colors.black : Colors.grey),
              const SizedBox(width: 6),
              Text(
                text,
                style: TextStyle(
                    fontWeight: FontWeight.w600,
                    color: selected ? Colors.black : Colors.grey),
              ),
            ],
          ),
        ),
      ),
    );
  }

  Widget _sectionTitle(String title) {
    return Align(
      alignment: Alignment.centerLeft,
      child: Text(
        title.toUpperCase(),
        style: const TextStyle(
            fontSize: 12,
            fontWeight: FontWeight.bold,
            color: Colors.grey),
      ),
    );
  }

  Widget _statCard(String number, String label) {
    return Container(
      padding: const EdgeInsets.all(20),
      decoration: BoxDecoration(
        borderRadius: BorderRadius.circular(16),
        boxShadow: const [
          BoxShadow(
              blurRadius: 6,
              color: Colors.black12,
              offset: Offset(0, 2))
        ],
        color: Colors.white,
      ),
      child: Column(
        children: [
          Text(number,
              style: const TextStyle(
                  fontSize: 28,
                  fontWeight: FontWeight.bold)),
          const SizedBox(height: 5),
          Text(label,
              style: const TextStyle(
                  fontSize: 11,
                  fontWeight: FontWeight.bold,
                  color: Colors.grey)),
        ],
      ),
    );
  }

  Widget _reviewCard() {

  if (reviewLoading) {
    return const Center(child: CircularProgressIndicator());
  }

  if (reviews.isEmpty) {
    return Container(
      padding: const EdgeInsets.all(20),
      decoration: BoxDecoration(
        borderRadius: BorderRadius.circular(16),
        boxShadow: const [
          BoxShadow(
              blurRadius: 6,
              color: Colors.black12,
              offset: Offset(0, 2))
        ],
        color: Colors.white,
      ),
      child: const Text("No reviews yet"),
    );
  }

  return Container(
    padding: const EdgeInsets.all(20),
    decoration: BoxDecoration(
      borderRadius: BorderRadius.circular(16),
      boxShadow: const [
        BoxShadow(
            blurRadius: 6,
            color: Colors.black12,
            offset: Offset(0, 2))
      ],
      color: Colors.white,
    ),
    child: Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [

        Text(
          averageRating.toStringAsFixed(1),
          style: const TextStyle(
              fontSize: 40,
              fontWeight: FontWeight.bold),
        ),

        const SizedBox(height: 5),

        Row(
          children: List.generate(5, (index) {
            return Icon(
              index < averageRating.round()
                  ? Icons.star
                  : Icons.star_border,
              color: const Color(0xFFFFB544),
            );
          }),
        ),

        const SizedBox(height: 5),

        Text(
          "${reviews.length} Reviews",
          style: const TextStyle(color: Colors.grey),
        ),
      ],
    ),
  );
}

  Widget _serviceChip(String text) {
    return Container(
      padding:
          const EdgeInsets.symmetric(horizontal: 12, vertical: 8),
      decoration: BoxDecoration(
        color: lightCream,
        borderRadius: BorderRadius.circular(10),
      ),
      child: Text(text,
          style: const TextStyle(fontWeight: FontWeight.w600)),
    );
  }

  Widget _bigButton(String text, Color color) {
    return SizedBox(
      width: double.infinity,
      child: ElevatedButton(
        style: ElevatedButton.styleFrom(
          backgroundColor: color,
          padding:
              const EdgeInsets.symmetric(vertical: 16),
          shape: RoundedRectangleBorder(
              borderRadius: BorderRadius.circular(14)),
        ),
        onPressed: () {},
        child: Text(text,
            style: const TextStyle(
                fontWeight: FontWeight.bold,
                fontSize: 14,
                color:Colors.white,)),
      ),
    );
  }
}